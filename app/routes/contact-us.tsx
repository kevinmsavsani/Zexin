import type { MetaFunction } from "@remix-run/node";
import { Form, useActionData } from "@remix-run/react";
import { json } from "@remix-run/node";
import type { ActionFunction } from "@remix-run/node";
import { CLIENT_EMAIL, CLIENT_PASSWORD } from "env";
import nodemailer from "nodemailer";
import { toast } from "react-toastify";
import { useEffect } from "react";
export const meta: MetaFunction = () => {
  return [
    { title: "New Remix App" },
    { name: "description", content: "Welcome to Remix!" },
  ];
};

// Configure the email transporter
const transporter = nodemailer.createTransport({
  service: "gmail",
  auth: {
    user: CLIENT_EMAIL,
    pass: CLIENT_PASSWORD, // Use environment variables in production
  },
});

export const action: ActionFunction = async ({
  request,
}) => {
  const formData = await request.formData();
  const firstName = formData.get("first-name");
  const lastName = formData.get("last-name");
  const company = formData.get("company");
  const email = formData.get("email");
  const phoneNumber = formData.get("phone-number");
  const message = formData.get("message");

  const mailOptions = {
    from: CLIENT_EMAIL,
    to: "kevinmsavsani@gmail.com",
    subject: "Contact Form Submission",
    text: `Name: ${firstName} ${lastName}\nCompany: ${company}\nEmail: ${email}\nPhone Number: ${phoneNumber}\nMessage: ${message}`,
  };

  try {
    await transporter.sendMail(mailOptions);
    return json({
      success: true,
      msg: "Successfully send email. Will get back to you soon.",
    });
  } catch (error) {
    console.error("Error sending email:", error);
    return json(
      { success: false, msg: "Failed to send email." },
      { status: 500 }
    );
  }
};

